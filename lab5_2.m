bf=kf*max(abs(m))/W;

%Ύστερα θα υπολογίσουμε  τον δείκτη διαμόρφωσης FM από την εξίσωση βf=kf*α/W,που ισχύει για ημιτονοειδή σήματα πληροφορίας 
