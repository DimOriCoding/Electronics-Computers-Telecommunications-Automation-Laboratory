%’ρα ακολουθώντας την παραπάνω ιδιότητα:
%Μ(j*2*π*f)=(0,01)*(u(f+50)-u(f-50)),όπου u(f) η συνάρτηση παλμού,δηλαδή η διαφορά δύο τετραγωνικών παλμών που
