%αποτέλεσμα και με τους δύο τρόπους για κάθε σήμα, δηλαδή: 
%Όσον αφορά το σήμα πληροφορίας m(t) θα ισχύει ότι:
%Από την ιδιότητα συμμετρίας του Μ/Σ Fourier είναι γνωστό ότι αν ο Μ/Σ Fourier ενός σήματος x(t) είναι X(ω), 
